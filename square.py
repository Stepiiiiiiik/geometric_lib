def area(a):
    '''
    Возвращает площадь квадрата

            Параметры:
                a (int) : сторона квадрата

            Возвращаемое значение:
                Площадь квадрата, заданного стороной
    '''

    return abs(a) * abs(a)


def perimeter(a):
    '''
    Возвращает периметр квадрата

            Параметры:
                a (int) : сторона квадрата

            Возвращаемое значение:
                Периметр квадрата, заданного стороной
    '''

    return 4 * abs(a)
